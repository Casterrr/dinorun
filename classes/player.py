class Player():
    def __init__(self):
        self.nome = str()
        self.score = int()
    
    def get_nome(self):
        '''
        Retorna nome do jogador.
        '''

        return str(self.nome)
    
    def get_score(self):
        '''
        Retorna score do jogador.
        '''

        return str(self.score)